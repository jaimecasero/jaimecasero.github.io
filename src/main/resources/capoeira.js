// create web audio api context
const audioCtx = new (window.AudioContext || window.webkitAudioContext);
var sound_delay = 80;
var currentTime = 0;
const MAX_NOTE = 18;
const MAX_BEATS = 16;
const gainNodes = [];
const pannerNodes = [];


function setupGains() {
    for (let i = 0; i < audioBuffers.length; i++) {
        const gainNode = audioCtx.createGain();
        gainNode.connect(audioCtx.destination);
        gainNodes[i] = gainNode;
        // Configuración de paneo estéreo
        const pannerNode = audioCtx.createStereoPanner();
        pannerNode.connect(gainNode); // Conecta el panner al nodo de ganancia
        pannerNodes[i] = pannerNode;
        pannerNode.pan.value= 1;
        if (i < 9) {
            pannerNode.pan.value= -1;
        }
    }
    console.log("gain/panners nodes set up");
}

function stop() {
    if (timerID) {
        clearTimeout(timerID);
        timerID = null;
    }
}

let audioBuffers = [];

async function loadSounds() {
    for (let i = 0; i < soundFiles.length; i++) {
        const response = await fetch(soundFiles[i]);
        const arrayBuffer = await response.arrayBuffer();
        const buffer = await audioCtx.decodeAudioData(arrayBuffer);
        audioBuffers[i] = buffer;
    }
    console.log("sounds loaded");
}


async function initAudio() {
    console.log("init audio");
    console.log("audio started")
    await loadSounds();
    setupGains();
}
