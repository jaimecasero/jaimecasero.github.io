function changeBeat() {
    var beatIndex = beatSelect.value;
    console.log("beat" + beatIndex);
    currentBeat = beatArray[beatIndex];
    bpmInput.value = beatBPMArray[beatIndex];
    changeBpm();
    renderBeatArray();
}

function renderBeatArray() {
    for (var i = 0; i < instrumentTable.getElementsByTagName("tr").length; i++) {
        var tr = instrumentTable.getElementsByTagName("tr")[i + 1];
        if (tr !== undefined) {
            for (var j = 0; j < MAX_BEATS; j++) {
                var td = tr.getElementsByTagName("td")[j + 1];
                var tdButton = td.getElementsByTagName("input")[0];
                if (currentBeat[i][j]) {
                    tdButton.style.background = "orangered";
                } else {
                    tdButton.style.background = "white";

                }
            }
        }
    }
}

function changeBpm() {
    sound_delay = (60000 / bpmInput.value) / 4;
    console.log("delay" + sound_delay);
}

function changeViola() {
    if (violaSelect.value < 0) {
        //set viola same as gunga
        currentBeat[6] = currentBeat[0];
        currentBeat[7] = currentBeat[1];
        currentBeat[8] = currentBeat[2];
    } else {
        //set viol to corresponding viola array position
        currentBeat[6] = violaArray[violaSelect.value][0];
        currentBeat[7] = violaArray[violaSelect.value][1];
        currentBeat[8] = violaArray[violaSelect.value][2];

    }
    renderBeatArray();
}

function changeVolume(volumeLevel, instrumentArray) {
    console.log("volume:" + volumeLevel + " instrumentArray:" + instrumentArray);
    const newVolume = volumeLevel / 100;
    for (let i = 0; i < instrumentArray.length; i++) {
        const index = instrumentArray[i];
        if (gainNodes[index]) {
            gainNodes[index].gain.value = newVolume;
        }
    }
}


function changeNote(tdButton) {

    currentBeat[tdButton.parentElement.parentElement.rowIndex - 1][tdButton.parentElement.cellIndex - 1] = !currentBeat[tdButton.parentElement.parentElement.rowIndex - 1][tdButton.parentElement.cellIndex - 1];
    renderBeatArray();
}

function muteNote(tdButton, instrumentIndex) {
    console.log("instrumentIndex:" + instrumentIndex);

    if (tdButton.parentElement.style.background === "green") {
        tdButton.parentElement.style.background = "red";
        changeVolume(1, [instrumentIndex]);

    } else {
        tdButton.parentElement.style.background = "green";
        changeVolume(50, [instrumentIndex]);

    }
}


let scheduleAheadTime = 0.1; // seconds
let lookahead = 25; // ms
let nextNoteTime;

function scheduler() {
    while (nextNoteTime < audioCtx.currentTime + scheduleAheadTime) {
        scheduleNote(currentTime, nextNoteTime);
        nextNote();
    }
    timerID = setTimeout(scheduler, lookahead);
}

function scheduleNote(index, when) {
    for (let i = 0; i < MAX_NOTE; i++) {
        if (currentBeat[i][index]) {
            const source = audioCtx.createBufferSource();
            source.buffer = audioBuffers[i];
            source.connect(gainNodes[i]);
            source.start(when);
        }
    }

    // Delay UI update slightly to match audio
    setTimeout(() => renderNextColumn(index), (when - audioCtx.currentTime) * 1000);
}

function nextNote() {
    nextNoteTime += sound_delay / 1000;
    currentTime++;
    if (currentTime >= currentBeat[0].length) currentTime = 0;
}

function startPlayback() {
    nextNoteTime = audioCtx.currentTime;
    audioCtx.resume().then(() => {
        loadSounds().then(() => {
            setupGains();
            currentTime = 0;
            nextNoteTime = audioCtx.currentTime;
            scheduler();
        });
    });
}

